// Read CSV
const csvContent = fs.readFileSync('songs-export.csv', 'utf-8');
const lines = csvContent.split('\n').filter(line => line.trim());

// Update header
const newHeader = 'Movie Name,Song Name,Music Director,Actor Name,Actress Name,Filename';
const updatedLines = [newHeader];

console.log(`Processing ${lines.length - 1} songs...`);

// Process each line (skip header)
for (let i = 1; i < lines.length; i++) {
  const line = lines[i].trim();
  if (!line) continue;
  
  // Simple CSV parser - split but handle quotes
  const parts = [];
  let current = '';
  let inQuotes = false;
  
  for (let j = 0; j < line.length; j++) {
    const char = line[j];
    if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === ',' && !inQuotes) {
      parts.push(current);
      current = '';
    } else {
      current += char;
    }
  }
  parts.push(current);
  
  if (parts.length < 4) {
    console.warn(`Skipping invalid line ${i}: ${line.substring(0, 50)}...`);
    continue;
  }
  
  const movieName = parts[0].replace(/^"|"$/g, '').trim();
  const songName = parts[1].trim();
  const musicDirector = parts[2].trim();
  const filename = parts[parts.length - 1].trim();
  
  // Get actor/actress from mapping
  const cast = movieCastMap[movieName] || { actor: '', actress: '' };
  
  // Create updated line with proper CSV escaping
  const escapedMovie = movieName.includes(',') ? `"${movieName}"` : movieName;
  const escapedSong = songName.includes(',') ? `"${songName}"` : songName;
  const escapedMusic = musicDirector.includes(',') ? `"${musicDirector}"` : musicDirector;
  const escapedActor = cast.actor && cast.actor.includes(',') ? `"${cast.actor}"` : cast.actor;
  const escapedActress = cast.actress && cast.actress.includes(',') ? `"${cast.actress}"` : cast.actress;
  
  updatedLines.push(`${escapedMovie},${escapedSong},${escapedMusic},${escapedActor},${escapedActress},${filename}`);
}

// Write updated CSV
fs.writeFileSync('songs-export.csv', updatedLines.join('\n'));

// Count how many songs have actor/actress info
const withCast = updatedLines.slice(1).filter(line => {
  const parts = line.split(',');
  return parts.length >= 6 && parts[3] && parts[4];
}).length;

